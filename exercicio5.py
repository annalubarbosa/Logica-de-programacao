nota_um= float(input("Informe a nota do 1° Bimestre: "))
nota_dois =float(input("Informe a nota do 2° Bimestre: "))
nota_tres = float(input("Informe a nota do 3° Bimestre: "))
nota_quatro = float(input("Informe a nota do 4° Bimestre: "))

media = (nota_um *1  + nota_dois * 2 + nota_tres * 3 + nota_quatro * 4) / 10





print(f"A média da sua nota é:  {media}")
